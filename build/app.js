"use strict";
function toStringFunc(data) {
    if (Array.isArray(data)) {
        return data.toString();
    }
    switch (typeof data) {
        case 'string':
            return data;
        case 'number':
        case 'symbol':
        case 'bigint':
        case 'boolean':
        case 'function':
            return data.toString();
        case 'object':
            return JSON.stringify(data);
        default:
            return undefined;
    }
}
toStringFunc(1);
toStringFunc(true);
toStringFunc(['a', 'b']);
toStringFunc({ a: 1 });
